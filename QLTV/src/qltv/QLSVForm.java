    public void renderTableData(ResultSet resultSet) {
        DefaultTableModel tableModel = new DefaultTableModel();
        tableModel.addColumn("Mã SV");
        tableModel.addColumn("Họ Tên");
        tableModel.addColumn("Ngày Sinh");
        tableModel.addColumn("Giới Tính");
        tableModel.addColumn("Lớp");
        tableModel.addColumn("Khoa");
        tableModel.addColumn("Level");
        
        try {
            if (resultSet != null) {
                resultSet.beforeFirst();
                boolean hasData = false;
                while (resultSet.next()) {
                    hasData = true;
                    String maSV = resultSet.getString("MaSV");
                    String hoTen = resultSet.getString("Hoten");
                    String ngaySinh = resultSet.getString("Ngaysinh");
                    String gioiTinh = resultSet.getString("Gioitinh");
                    String lop = resultSet.getString("Lop");
                    String khoa = resultSet.getString("Khoa");
                    int level = resultSet.getInt("Level");

                    Object[] rowData = {maSV, hoTen, ngaySinh, gioiTinh, lop, khoa, level};
                    tableModel.addRow(rowData);
                }

                if (!hasData) {
                    JOptionPane.showMessageDialog(null, "Không có dữ liệu");
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        UserTable.setModel(tableModel);
    }
    
    public boolean CheckNull(){
        if(Level.getSelectedIndex() == 0) return true;
        if(MaSV.getText().equals("")) return true;
        if(Ngaysinh.getText().equals("")) return true;
        if(Hoten.getText().equals("")) return true;
        if(Lop.getText().equals("")) return true;
        if(!Nam.isSelected() && !Nu.isSelected()) return true;
        if("Null".equals(Khoa.getSelectedItem())) return true;
        return false;
    }
    
    public boolean CheckMaSV(){
        if(this.ad.CheckMaSV(MaSV.getText()) == 1) return true;
        return false;
    }

