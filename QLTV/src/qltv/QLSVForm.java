    private void UpdateUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateUserActionPerformed
        // TODO add your handling code here:
        if(this.CheckNull()){
            JOptionPane.showMessageDialog(null, "Nhập đầy đủ thông tin");
            return;
        }
        if(!temp.equals(MaSV.getText())){
            JOptionPane.showMessageDialog(null, "Bạn không được thay đổi mã sinh viên");
            return;
        }
        char[] CharPass = Password.getPassword();
        String Pass = new String(CharPass);
        Arrays.fill(CharPass, ' ');
        try {
            MessageDigest md = MessageDigest.getInstance("MD5");
            byte[] PasswordByte = Pass.getBytes();
            byte[] PasswordHash = md.digest(PasswordByte);
            StringBuilder hexString = new StringBuilder();
            for (byte b : PasswordHash) {
                String hex = String.format("%02x", b);
                hexString.append(hex);
            }
            int check = 0;
            if(Pass.equals("")){
                if(this.ad.UpdateUsers(MaSV.getText(), Hoten.getText(), Nam.isSelected() ? "Nam" : "Nữ", Ngaysinh.getText(), Lop.getText(), (String) Khoa.getSelectedItem(), Integer.parseInt((String) Level.getSelectedItem())) == 1){
                    check = 1;
                }
            }else{
                if(this.ad.UpdateUsers(MaSV.getText(), Hoten.getText(), Nam.isSelected() ? "Nam" : "Nữ", Ngaysinh.getText(), Lop.getText(), (String) Khoa.getSelectedItem(), Integer.parseInt((String) Level.getSelectedItem())) == 1 && this.ad.UpdatePassword(MaSV.getText(), hexString.toString()) == 1){
                    check = 1;
                }
            }
            if(check == 1){
                JOptionPane.showMessageDialog(null, "Sửa thành công");
                this.renderTableData(this.ad.GetAllUsers());
                return;
            }else{
                JOptionPane.showMessageDialog(null, "Sửa thất bại");
                return;
            }
        } catch (Exception e) {
            
        }
    }//GEN-LAST:event_UpdateUserActionPerformed

