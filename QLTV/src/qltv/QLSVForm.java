    private void SearchUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchUserActionPerformed
        // TODO add your handling code here:
        ConnectToSql conn = new ConnectToSql();
        ResultSet resultSet = null;
        try {
            StringBuilder queryBuilder = new StringBuilder("SELECT * FROM Users WHERE 1 = 1");

            if (!MaSV.getText().equals("")) {
                queryBuilder.append(" AND MaSV like '%").append(MaSV.getText()).append("%'");
            }

            if (!Hoten.getText().equals("")) {
                queryBuilder.append(" AND Hoten like '%").append(Hoten.getText()).append("%'");
            }
            
            if(!Ngaysinh.getText().equals("")){
                queryBuilder.append(" AND Ngaysinh like '%").append(Ngaysinh.getText()).append("%'");
            }
            
            if(Nam.isSelected() || Nu.isSelected()){
                String Text = Nam.isSelected() ? "Nam" : "Nữ";
                queryBuilder.append(" AND Gioitinh = N'").append(Text).append("'");
            }
            
            if(!Lop.getText().equals("")){
                queryBuilder.append(" AND Lop like '%").append(Lop.getText()).append("%'");
            }
            
            if(!"Null".equals((String) Khoa.getSelectedItem())){
                queryBuilder.append(" AND Khoa = '").append((String) Khoa.getSelectedItem()).append("'");
            }

            if(!"Null".equals((String) Level.getSelectedItem())){
                queryBuilder.append(" AND Level = ").append((String) Level.getSelectedItem());
            }
            
            String querySql = queryBuilder.toString();
            System.out.println(querySql);
            conn.statement = conn.connection.prepareStatement(querySql, ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            resultSet = conn.statement.executeQuery();
            if(resultSet.next()){
                this.renderTableData(resultSet);
            }else{
                JOptionPane.showMessageDialog(null, "Không có dữ liệu thỏa mãn");
            }
        } catch (Exception e) {
            e.printStackTrace(); // hoặc sử dụng Logger để ghi log
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_SearchUserActionPerformed

    private void AddUsserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddUsserActionPerformed
        // TODO add your handling code here:
        if(this.CheckNull()){
            JOptionPane.showMessageDialog(null, "Nhập đầy đủ thông tin");
            return;
        }
        if(this.CheckMaSV()){
            JOptionPane.showMessageDialog(null, "Mã sinh viên đã tồn tại");
            return;
        }
        if(!this.ad.isValidDateFormat(Ngaysinh.getText(), "dd-mm-yyy")){
            JOptionPane.showMessageDialog(null, "Ngày sinh theo định dạng dd-mm-yyyy");
            return;
        }
        char[] CharPass = Password.getPassword();
        String Pass = new String(CharPass);
        Arrays.fill(CharPass, ' ');
        if(Pass.equals("")){
            JOptionPane.showMessageDialog(null, "Nhập đầy đủ thông tin");
            return;
        }
        try {
            MessageDigest md = MessageDigest.getInstance("MD5");
            byte[] PasswordByte = Pass.getBytes();
            byte[] PasswordHash = md.digest(PasswordByte);
            StringBuilder hexString = new StringBuilder();
            for (byte b : PasswordHash) {
                String hex = String.format("%02x", b);
                hexString.append(hex);
            }
            if(this.ad.AddUsers(MaSV.getText(), hexString.toString(), Hoten.getText(), Nam.isSelected() ? "Nam" : "Nữ", Ngaysinh.getText(), Lop.getText(), (String) Khoa.getSelectedItem(), Integer.parseInt((String) Level.getSelectedItem())) == 1){
                JOptionPane.showMessageDialog(null, "Thêm thành công");
                this.renderTableData(this.ad.GetAllUsers());
                return;
            }else{
                JOptionPane.showMessageDialog(null, "Thêm thất bại");
                return;
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e);
            return;
        }
    }//GEN-LAST:event_AddUsserActionPerformed
    
    private void initializeUI(){
        ListSelectionModel selectionModel = UserTable.getSelectionModel();
        selectionModel.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        selectionModel.addListSelectionListener(new ListSelectionListener(){
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(!e.getValueIsAdjusting()){
                    int selectedRow = UserTable.getSelectedRow();
                    if(selectedRow != -1){
                        Object maSV = UserTable.getValueAt(selectedRow, 0);
                        Object hoTen = UserTable.getValueAt(selectedRow, 1);
                        Object ngaySinh = UserTable.getValueAt(selectedRow, 2);
                        Object gioiTinh = UserTable.getValueAt(selectedRow, 3);
                        Object lop = UserTable.getValueAt(selectedRow, 4);
                        Object khoa = UserTable.getValueAt(selectedRow, 5);
                        Object level = UserTable.getValueAt(selectedRow, 6);
                        MaSV.setText(maSV.toString());
                        temp = maSV.toString();
                        Hoten.setText(hoTen.toString());
                        Ngaysinh.setText(ngaySinh.toString());
                        if(gioiTinh.toString().equals("Nam")){
                            Nam.setSelected(true);
                        }else{
                            Nu.setSelected(true);
                        }
                        Lop.setText(lop.toString());
                        Khoa.setSelectedItem(khoa.toString());
                        Level.setSelectedItem(level.toString());
                    }
                }
            }
            
        });
    }
    
