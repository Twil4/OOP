    private void DeleteSachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteSachActionPerformed
        // TODO add your handling code here:
        if(Masach.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Chọn sách cần xóa");
            return;
        }
        if(this.ad.CheckMaSach(Masach.getText()) == 0){
            JOptionPane.showMessageDialog(null, "Mã sách không tồn tại");
            return;
        }
        if(this.ad.DeleteSach(Masach.getText()) == 1){
            this.ad.DeleteMSWithMaSach(Masach.getText());
            JOptionPane.showMessageDialog(null, "Xóa thành công");
            Masach.setText("");
            Tensach.setText("");
            Theloai.setSelectedIndex(0);
            Soluong.setValue(-1);
            this.renderTableData(this.ad.GetAllSach());
            return;
        }else{
            JOptionPane.showMessageDialog(null, "Xóa thất bại");
        }
    }//GEN-LAST:event_DeleteSachActionPerformed

