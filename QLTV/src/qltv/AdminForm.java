    public void renderTableData(ResultSet resultSet) {
        DefaultTableModel tableModel = new DefaultTableModel();
        tableModel.addColumn("Mã Sách");
        tableModel.addColumn("Tên Sách");
        tableModel.addColumn("Thể Loại");
        tableModel.addColumn("Số Lượng");
        
        try {
            if (resultSet != null) {
                resultSet.beforeFirst();
                boolean hasData = false;
                while (resultSet.next()) {
                    hasData = true;
                    String maSach = resultSet.getString("Masach");
                    String tenSach = resultSet.getString("Tensach");
                    String theLoai = resultSet.getString("Theloai");
                    int soLuong = resultSet.getInt("Soluong");

                    Object[] rowData = {maSach, tenSach, theLoai, soLuong};
                    tableModel.addRow(rowData);
                }

                if (!hasData) {
                    JOptionPane.showMessageDialog(null, "Không có dữ liệu");
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        TableSach.setModel(tableModel);
    }
    
    public boolean CheckNull(){
        if (Masach.getText().equals("")) return true;
        if(Tensach.getText().equals("")) return true;
        if(Theloai.getSelectedIndex() == 0) return true;
        if(Soluong.getValue().equals("")) return true;
        return false;
    }

