    private void initializeUI(){
        ListSelectionModel selectionModel = TableSach.getSelectionModel();
        selectionModel.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        selectionModel.addListSelectionListener(new ListSelectionListener(){
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(!e.getValueIsAdjusting()){
                    int selectedRow = TableSach.getSelectedRow();
                    if(selectedRow != -1){
                        Object idSach = TableSach.getValueAt(selectedRow, 0);
                        Object tenSach = TableSach.getValueAt(selectedRow, 1);
                        Object theLoai = TableSach.getValueAt(selectedRow, 2);
                        Object soLuong = TableSach.getValueAt(selectedRow, 3);
                        Masach.setText(idSach.toString());
                        Tensach.setText(tenSach.toString());
                        Theloai.setSelectedItem(theLoai.toString());
                        Soluong.setValue(Integer.parseInt(soLuong.toString()));
                    }
                }
            }
            
        });
    }
    
