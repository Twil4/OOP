    private void AddMSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddMSActionPerformed
        // TODO add your handling code here:
        if(this.CheckNull()){
            JOptionPane.showMessageDialog(null, "Nhập đầy đủ thông tin");
            return;
        }
        if(this.ad.CheckMaSV(MaSV.getText()) == 0){
            JOptionPane.showMessageDialog(null, "Mã sinh viên không tồn tại");
            return;
        }
        if(this.ad.CheckMaSach(Integer.parseInt(Masach.getText())) == 0){
            JOptionPane.showMessageDialog(null, "Mã sách không tồn tại");
            return;
        }
        if(this.ad.CheckSoLuongSach(Integer.parseInt(Masach.getText()), Integer.parseInt(Soluong.getText())) == 1){
            JOptionPane.showMessageDialog(null, "Số lượng sách không đủ");
            return;
        }
        String MaMS = MaSV.getText() + Masach.getText();
        if(this.ad.AddMS(MaMS, MaSV.getText(), Integer.parseInt(Masach.getText()), Ngayhentra.getText(), Integer.parseInt(Soluong.getText())) == 1 && this.ad.UpdateSoLuongSach(Integer.parseInt(Masach.getText()), Integer.parseInt(Soluong.getText())) == 1){
            JOptionPane.showMessageDialog(null, "Thêm thành công");
            this.renderTableData(this.ad.GetAllMuonSach());
            return;
        }else{
            JOptionPane.showMessageDialog(null, "Thêm thất bại");
            return;
        }
    }//GEN-LAST:event_AddMSActionPerformed

