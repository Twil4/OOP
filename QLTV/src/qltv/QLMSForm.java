    private void UpdateMSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateMSActionPerformed
        // TODO add your handling code here:
        if(this.CheckNull() || Mamuonsach.getText().equals("") || Ngaymuon.getText().equals("") || Tinhtrang.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null, "Nhập đầy đủ thông tin");
            return;
        }
        if(this.ad.CheckMaSV(MaSV.getText()) == 0){
            JOptionPane.showMessageDialog(null, "Mã sinh viên không tồn tại");
            return;
        }
        if(this.ad.CheckMaSach(Integer.parseInt(Masach.getText())) == 0){
            JOptionPane.showMessageDialog(null, "Mã sách không tồn tại");
            return;
        }
        if(this.ad.CheckMaMS(Mamuonsach.getText()) == 0){
            JOptionPane.showMessageDialog(null, "Mã mượn sách không tồn tại");
            return;
        }
        if(this.ad.GetMaSach(Mamuonsach.getText()) != Integer.parseInt(Masach.getText())){
            JOptionPane.showMessageDialog(null, "Không được thay đổi mã sách");
            return;
        }
        int bit = 0;
        if(Tinhtrang.getSelectedIndex() == 1){
            bit = 1;
        }
        if (Tinhtrang.getSelectedIndex() == 2) {
            bit = 0;
        }
        int Dangmuon = this.ad.GetSoLuongMuon(Mamuonsach.getText());
        int Soluongcon = this.ad.GetSoLuongSachCon(Integer.parseInt(Masach.getText()));
        Soluongcon = Soluongcon + Dangmuon;
        if(this.ad.UpdateSLSach(Integer.parseInt(Masach.getText()), Soluongcon) == 0){
            JOptionPane.showMessageDialog(null, "Có lỗi xảy ra");
            return;
        }
        if(bit == 1){
            if(this.ad.UpdateMS(Mamuonsach.getText(), Integer.parseInt(Masach.getText()), MaSV.getText(), Ngaymuon.getText(), Ngayhentra.getText(), bit) == 1 && this.ad.UpdateNgayTra(Mamuonsach.getText(), Ngaytra.getText()) == 1){
                JOptionPane.showMessageDialog(null, "Sửa thành công");
                this.renderTableData(this.ad.GetAllMuonSach());
                return;
            }else{
                JOptionPane.showMessageDialog(null, "Sửa thất bại");
                return;
            }
        }else{
            if(this.ad.UpdateMS(Mamuonsach.getText(), Integer.parseInt(Masach.getText()), MaSV.getText(), Ngaymuon.getText(), Ngayhentra.getText(), bit) == 1 && this.ad.UpdateSLSach(Integer.parseInt(Masach.getText()), Soluongcon - Integer.parseInt(Soluong.getText())) == 1 && this.ad.UpdateSLTraSach(Mamuonsach.getText(), Integer.parseInt(Soluong.getText())) == 1){
                JOptionPane.showMessageDialog(null, "Sửa thành công");
                this.renderTableData(this.ad.GetAllMuonSach());
                return;
            }else{
                JOptionPane.showMessageDialog(null, "Sửa thất bại");
                return;
            }
        }
        
    }//GEN-LAST:event_UpdateMSActionPerformed
    
    
    private void initializeUI(){
        ListSelectionModel selectionModel = MSTable.getSelectionModel();
        selectionModel.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        selectionModel.addListSelectionListener(new ListSelectionListener(){
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(!e.getValueIsAdjusting()){
                    int selectedRow = MSTable.getSelectedRow();
                    if(selectedRow != -1){
                        Object mamuon = MSTable.getValueAt(selectedRow, 0);
                        Object masach = MSTable.getValueAt(selectedRow, 1);
                        Object masv = MSTable.getValueAt(selectedRow, 2);
                        Object soluong = MSTable.getValueAt(selectedRow, 3);
                        Object ngaymuon = MSTable.getValueAt(selectedRow, 4);
                        Object ngayhentra = MSTable.getValueAt(selectedRow, 5);
                        Object ngaytra = MSTable.getValueAt(selectedRow, 6);
                        Object tinhtrang = MSTable.getValueAt(selectedRow, 7);
                        Mamuonsach.setText(mamuon.toString());
                        Ngaymuon.setText(ngaymuon.toString());
                        MaSV.setText(masv.toString());
                        Ngayhentra.setText(ngayhentra.toString());
                        if(ngaytra != null){
                            Ngaytra.setText(ngaytra.toString());
                            Tinhtrang.setSelectedIndex(1);
                        }else{
                            Tinhtrang.setSelectedIndex(2);
                        }
                        Soluong.setText(soluong.toString());
                        Masach.setText(masach.toString());
                    }
                }
            }
            
        });
    }
    
