    private void fAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fAddActionPerformed
        // TODO add your handling code here:
        if(this.CheckNull()){
            JOptionPane.showMessageDialog(null, "Không được để trống thông tin");
            return;
        }
        NhanVien nv;
        try{
            nv = new NhanVien(fHoten.getText(),
            fNgaysinh.getText(),
            fDiachi.getText(),
            fNam.isSelected() ? "Nam" : "Nu",
            fPhongban.getText(),
            Float.parseFloat(fHeSL.getText()),
            Integer.parseInt(fLuonngCB.getText()),
            Float.parseFloat(fThamNien.getText()));
            
        }catch(NumberFormatException ex){
            JOptionPane.showConfirmDialog(null, ex);
            return;
        }
        
        FileWriter fw = null;
        try {
            fw = new FileWriter(":sinhvien.dat");
            BufferedWriter bw = new BufferedWriter(fw);
            bw.append(nv.getHoten() + " - " + nv.getNgaysinh() + " - " + nv.getDiachi() + " - " + nv.getGioitinh() + " - " + nv.getPhongban() + " - " + nv.getHeSL() + " - " + nv.getLuongCB() + " - " + nv.getThamnien() + " - " + nv.LuongThucLinh());
            bw.newLine();
            bw.close();
            fw.close();
            JOptionPane.showMessageDialog(null, "Thêm thành công");
        } catch (IOException ex) {
            Logger.getLogger(Form.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_fAddActionPerformed
    
