    public Boolean CheckNull(){
        if(fHoten.getText().equals("")) return true;
        if(fNgaysinh.getText().equals("")) return true;
        if(fDiachi.getText().equals("")) return true;
        if(!fNam.isSelected() && !fNu.isSelected()) return true;
        if(fPhongban.getText().equals("")) return true;
        if(fHeSL.getText().equals("")) return true;
        if(fLuonngCB.getText().equals("")) return true;
        if(fThamNien.getText().equals("")) return true;
        return false;
    }

